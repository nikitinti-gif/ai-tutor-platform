def build_action_plan(decision: dict) -> list[str]:
    decision_type = decision.get("decision_type")
    topic = decision.get("priority", "текущая тема")

    if decision_type == "teacher_review":
        return [
            "Передать решение преподавателю на ручную проверку.",
            "Не отправлять ученику автоматический вывод как окончательный.",
        ]

    if decision_type == "repeat_skill":
        return [
            f"Повторить теорию по теме «{topic}» 10 минут.",
            "Дать 3 базовые задачи.",
            "Через 2 дня дать контрольную задачу на этот же навык.",
            "Уведомить преподавателя о повторяющейся ошибке.",
        ]

    if decision_type == "guided_correction":
        return [
            "Задать ученику один наводящий вопрос.",
            f"Добавить 1–2 похожие задачи по теме «{topic}».",
            "Проверить повторно после исправления.",
        ]

    if decision_type == "increase_difficulty":
        return [
            f"Закрепить тему «{topic}» одной задачей среднего уровня.",
            "Постепенно повысить сложность следующего задания.",
            "Начислить ученику XP за успешное решение.",
        ]

    return [
        "Собрать больше данных об ученике.",
        "Не менять траекторию до появления новых сигналов.",
    ]